    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try
       {
           Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/payroll","root","Aditya26@");
        String sql = "select * from department where did = ?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1,Integer.parseInt(txtdid.getText()));
        
        ResultSet rs = ps.executeQuery();
        if(rs.next())
        {
            txtdname.setText(rs.getString("dname"));
            
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Record Not Found");
        }
        con.close();
       }
       catch(Exception e)
       {
           System.out.println(e);
       }
    }//GEN-LAST:event_jButton4ActionPerformed

