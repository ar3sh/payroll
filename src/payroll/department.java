        String did = txtdid.getText();
        String name = txtdname.getText();
        
        
        int id =Integer.parseInt(did);
        //int ephn =Integer.parseInt(phn);
        
        try
       {
           Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/payroll","root","Aditya26@");
            Statement stmnt = con.createStatement();
         String sql2 = "SELECT * FROM department WHERE did="+id+" ";
         ResultSet rs1 = stmnt.executeQuery(sql2);
         
         if(rs1.next())
         {
             JOptionPane.showMessageDialog(this,"did: "+id+" Already Exists");
             txtdid.setText("");
             txtdname.setText("");
          
         }
         else
         {
              String sql = "INSERT INTO department VALUES("+id+",'"+name+"')";
              stmnt.executeUpdate(sql);
              JOptionPane.showMessageDialog(this,"Employee Record Inserted");
              txtdid.setText("");
              txtdname.setText("");
              
         }
         con.close();
         
       }
       catch(Exception e)
       {
           System.out.println(e);
       }
